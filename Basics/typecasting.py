# using typecasting to change the data type of a reference of a variable 

# int to float
x = 2
y = float(x)
print(y)

# float to int
x = 2.0
y = int(x)
print(y)

#int to str
x = 2
y = str(x)
print(y)

# float to str
x = 2.0
y = str(x)
print(y)

#str to float
x = '2'
y = float(x)
print(y)

#str to int
x = '2'
y = int(x)
print(y)