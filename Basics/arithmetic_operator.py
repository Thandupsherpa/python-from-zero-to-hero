#Arithmetic operators are used with numeric values to perform common mathematical operations

x = int(input("Enter first number: "))
y = int(input("Enter second number: "))

#addition
add = x + y
print(add)

#subtraction
sub = x - y
print(sub)

#multiplication
mul = x * y
print(mul)

#division
div = x / y
print(div)

#modulus
mod = x % y
print(mod)

#Exponentiation
exp = x ** y
print(exp)

#floor division
floor = x // y
print(floor)

