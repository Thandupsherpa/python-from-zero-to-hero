#Assignment operators are used to assign values to variables

# = operator
x = 5
print(x)

# += operator
x += 3 # x = x + 3
print(x)

# -= operator
x -= 3 # x = x - 3
print(x)

# *= operator
x *= 3 # x = x * 3
print(x)

# /= operator
x /= 3 # x = x / 3
print(x)

# %= operator
x %= 3 # x = x % 3
print(x)

# //= operator
x //= 3 # x = x // 3
print(x)

# **= operator
x **= 3 # x = x ** 3
print(x)

# &= operator
y = 5
y &= 3 # x = x & 3
print(y)

# |= operator
x = 5
x |= 3 # x = x | 3
print(x)

# ^= operator
x = 5
x ^= 3 # x = x ^ 3
print(x)

# >>= operator
x = 5
x >>= 3 # x = x >> 3
print(x)

# <<= operator
x = 5
x <<= 3 # x = x << 3
print(x)

# := operator
print(x:=3) # x = 3
